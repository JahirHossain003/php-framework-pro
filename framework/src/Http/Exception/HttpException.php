<?php

namespace Jahir\Framework\Http\Exception;

class HttpException extends \Exception
{
    private int $status = 400;

    /**
     * @return int
     */
    public function getStatus(): int
    {
        return $this->status;
    }

    /**
     * @param int $status
     */
    public function setStatus(int $status): void
    {
        $this->status = $status;
    }
}